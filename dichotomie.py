def recherche_dichotomique(v, tableau):
    iter = 0
    max_iter = len(tableau)
    while tableau[0] != v:
        if iter < max_iter:
            print(f"tableau[0] : {tableau[0]}")
            iter += 1
            print(f"iter : {iter}")
            longueur_tableau = len(tableau)
            print(f"longueur_tab: {longueur_tableau}")
            mil = longueur_tableau // 2
            print(f"mil: {mil}")
            if tableau[mil] < v:
                print(f"tableau[mil] = {tableau[mil]} < {v}:")
                tableau = tableau[mil:longueur_tableau]
            elif tableau[mil] > v:
                print(f"tableau[mil] = {tableau[mil]} >= {v}:")
                tableau = tableau[0:mil+1]
            else:
                print(tableau)
                return iter
        else:
            return -1




